import supabase from './supabase';

export const getUserProfile = async () => {
  const {
    data: { user },
  } = await supabase.auth.getUser();

  if (!user) return null;

  const { data } = await supabase
    .from('user_profiles')
    .select('subscription_status')
    .eq('id', user.id)
    .single();

  return data;
};
